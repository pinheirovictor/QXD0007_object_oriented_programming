package br.sistema.aluno.ufc;

public class Avaliacao {
	// Atributos
	private float ap1;
	private float ap2;

	// Construtor
	public Avaliacao(float ap1, float ap2) {
		super();
		this.ap1 = ap1;
		this.ap2 = ap2;
	}

	// Get e Set
	public float getAp1() {
		return ap1;
	}

	public void setAp1(float ap1) {
		this.ap1 = ap1;
	}

	public float getAp2() {
		return ap2;
	}

	public void setAp2(float ap2) {
		this.ap2 = ap2;
	}

	

}
